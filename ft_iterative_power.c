int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	output;

	i = 1;
	output = nb;
	if (power < 0)
	{
		return (0);
	}
	if (power == 0)
	{
		return (1);
	}
	while (i < power)
	{
		output = output * nb;
		i++;
	}
	return (output);
}
